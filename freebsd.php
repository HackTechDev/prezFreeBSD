<section style="text-align: left;">
    <h3>FreeBSD</h3>
    <p>
<li>Système d'exploitation UNIX libre basé sur 386BSD</li>
<li>Free qui signifie à la fois « libre » et « gratuit » dans l'anglais courant</li>
<li>Berkeley software distribution (BSD), UNIX développé à l'université de Berkeley (San Francisco)</li>
<li>Version 1.0 est disponible en production dès novembre 1993</li>
<li>Dernière version stable : 13.0 (avril 2021)</li>
    </p>
</section>

